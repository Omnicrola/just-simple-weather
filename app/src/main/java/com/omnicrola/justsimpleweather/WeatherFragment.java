    private OnFragmentInteractionListener mListener;
    private DataStorageService dataStorageService;
    private WeatherDisplayAdapter displayAdapter;

    public WeatherFragment() {
        // Required empty public constructor
    }

    /**
     * Use this factory method to create a new instance of
     * this fragment using the provided parameters.
     *
     * @return A new instance of fragment WeatherFragment.
     */
    // TODO: Rename and change types and number of parameters
    public static WeatherFragment newInstance() {
        WeatherFragment fragment = new WeatherFragment();
        Bundle args = new Bundle();
        fragment.setArguments(args);
        return fragment;
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        return inflater.inflate(R.layout.fragment_weather, container, false);
    }

    // TODO: Rename method, update argument and hook method into UI event
    public void onButtonPressed(Uri uri) {
        if (mListener != null) {
            mListener.onFragmentInteraction(uri);
        }
    }

    @Override
    public void onAttach(Context context) {
        super.onAttach(context);
        this.dataStorageService = new DataStorageService(getActivity().getApplicationContext());
        this.displayAdapter = new WeatherDisplayAdapter(getActivity());
        if (context instanceof OnFragmentInteractionListener) {
            mListener = (OnFragmentInteractionListener) context;
        } else {
            throw new RuntimeException(context.toString()
                    + " must implement OnFragmentInteractionListener");
        }
    }

    @Override
    public void onStart() {
        super.onStart();
        updateWeatherFromStorage();
    }

    private void updateWeatherFromStorage() {
        Possible<WeatherReport> latestWeather = dataStorageService.getLatestWeather();
        if(latestWeather.isPresent()){
            WeatherReport weatherReport = latestWeather.get();
            this.displayAdapter.setDisplay(weatherReport);
        }
    }

    @Override
    public void onDetach() {
        super.onDetach();
        mListener = null;
    }

